#!/bin/envs Rscript
#install.packages('ggfortify')

library(ggfortify)
rma.data<-read.table("uniquenorm.txt",sep = "\t",header = T)
dataPCA<-t(rma.data)
dataPCA<-as.data.frame(dataPCA)
dataPCA$group<-c(rep("Case",3),rep("Ctrl",3))
#dataPCA$group<-c(rep("Case",2),rep("Ctrl",2),rep("treat",2))
dataPCA$group<-factor(dataPCA$group,levels=c("Case","Ctrl"))
pdf("PCA.pdf",height=4,width=4)
autoplot(prcomp(dataPCA[,-ncol(dataPCA)]),data=dataPCA,colour='group')
dev.off()

